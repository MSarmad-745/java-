public class Arthematic {
    public static void main(String[] args) {
        int a = 8;
        int b = 6;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);



        double c = 5;
        double d = 2;

        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);
        System.out.println(c%d);


    }
}
